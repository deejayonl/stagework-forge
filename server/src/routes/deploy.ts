// Helper to generate files for Vercel
const generateVercelFiles = (project: any, framework: string, filesToExport: any[]) => {
  const files: { file: string; data: string }[] = [];
  
  const seoTitle = project.seo?.title || project.name;
  const seoDescription = project.seo?.description || 'Generated by Stagework Forge';
  const seoFavicon = project.seo?.faviconUrl ? `\n    <link rel="icon" href="${project.seo.faviconUrl}" />` : '';
  const seoOgImage = project.seo?.ogImage ? `\n    <meta property="og:image" content="${project.seo.ogImage}" />` : '';
  const seoCustomMeta = project.seo?.metaTags ? `\n    ${project.seo.metaTags}` : '';

  if (framework === 'nextjs') {
    files.push({
      file: 'package.json',
      data: JSON.stringify({
        name: project.name.replace(/[^a-z0-9]/gi, '-').toLowerCase(),
        version: "0.1.0",
        private: true,
        scripts: {
          "dev": "next dev",
          "build": "next build",
          "start": "next start"
        },
        dependencies: {
          "next": "14.2.5",
          "react": "^18",
          "react-dom": "^18",
          "lucide-react": "^0.400.0"
        },
        devDependencies: {
          "tailwindcss": "^3.4.1",
          "postcss": "^8",
          "autoprefixer": "^10.0.1"
        }
      }, null, 2)
    });

    files.push({
      file: 'tailwind.config.js',
      data: `/** @type {import('tailwindcss').Config} */
module.exports = {
  content: [
    "./app/**/*.{js,ts,jsx,tsx,mdx}",
  ],
  theme: {
    extend: {},
  },
  plugins: [],
}`
    });

    files.push({
      file: 'app/globals.css',
      data: `@tailwind base;\n@tailwind components;\n@tailwind utilities;`
    });

    files.push({
      file: 'app/layout.jsx',
      data: `import './globals.css'\n\nexport const metadata = {\n  title: '${seoTitle}',\n  description: '${seoDescription}',\n}\n\nexport default function RootLayout({ children }) {\n  return (\n    <html lang="en">\n      <head>\n        ${seoFavicon}\n        ${seoOgImage}\n        ${seoCustomMeta}\n      </head>\n      <body>{children}</body>\n    </html>\n  )\n}`
    });

    let mainHtml = filesToExport.find((f: any) => f.name === 'index.html' || f.name.endsWith('.html'));
    if (mainHtml) {
      const bodyMatch = mainHtml.content.match(/<body[^>]*>([\s\S]*)<\/body>/i);
      const content = bodyMatch ? bodyMatch[1] : mainHtml.content;
      const jsxContent = content.replace(/class=/g, 'className=');

      files.push({
        file: 'app/page.jsx',
        data: `export default function Home() {\n  return (\n    <>\n      ${jsxContent}\n    </>\n  )\n}`
      });
    }

  } else if (framework === 'vite') {
    files.push({
      file: 'package.json',
      data: JSON.stringify({
        name: project.name.replace(/[^a-z0-9]/gi, '-').toLowerCase(),
        private: true,
        version: "0.0.0",
        type: "module",
        scripts: {
          "dev": "vite",
          "build": "vite build",
          "preview": "vite preview"
        },
        dependencies: {
          "react": "^18.3.1",
          "react-dom": "^18.3.1",
          "lucide-react": "^0.400.0"
        },
        devDependencies: {
          "@vitejs/plugin-react": "^4.3.1",
          "vite": "^5.3.1",
          "tailwindcss": "^3.4.4",
          "postcss": "^8.4.38",
          "autoprefixer": "^10.4.19"
        }
      }, null, 2)
    });

    files.push({
      file: 'vite.config.js',
      data: `import { defineConfig } from 'vite'\nimport react from '@vitejs/plugin-react'\n\nexport default defineConfig({\n  plugins: [react()],\n})`
    });

    files.push({
      file: 'tailwind.config.js',
      data: `/** @type {import('tailwindcss').Config} */\nexport default {\n  content: [\n    "./index.html",\n    "./src/**/*.{js,ts,jsx,tsx}",\n  ],\n  theme: {\n    extend: {},\n  },\n  plugins: [],\n}`
    });

    files.push({
      file: 'src/index.css',
      data: `@tailwind base;\n@tailwind components;\n@tailwind utilities;`
    });

    files.push({
      file: 'src/main.jsx',
      data: `import React from 'react'\nimport ReactDOM from 'react-dom/client'\nimport App from './App.jsx'\nimport './index.css'\n\nReactDOM.createRoot(document.getElementById('root')).render(\n  <React.StrictMode>\n    <App />\n  </React.StrictMode>,\n)`
    });

    let mainHtml = filesToExport.find((f: any) => f.name === 'index.html' || f.name.endsWith('.html'));
    if (mainHtml) {
      const bodyMatch = mainHtml.content.match(/<body[^>]*>([\s\S]*)<\/body>/i);
      const content = bodyMatch ? bodyMatch[1] : mainHtml.content;
      const jsxContent = content.replace(/class=/g, 'className=');

      files.push({
        file: 'src/App.jsx',
        data: `import React from 'react'\n\nfunction App() {\n  return (\n    <>\n      ${jsxContent}\n    </>\n  )\n}\n\nexport default App`
      });
    }

    files.push({
      file: 'index.html',
      data: `<!doctype html>\n<html lang="en">\n  <head>\n    <meta charset="UTF-8" />\n    <meta name="viewport" content="width=device-width, initial-scale=1.0" />\n    <title>${seoTitle}</title>\n    <meta name="description" content="${seoDescription}" />${seoFavicon}${seoOgImage}${seoCustomMeta}\n  </head>\n  <body>\n    <div id="root"></div>\n    <script type="module" src="/src/main.jsx"></script>\n  </body>\n</html>`
    });

  } else {
    // HTML
    filesToExport.forEach((file: any) => {
      if (file.type !== 'image' && !file.content.startsWith('data:image')) {
        let content = file.content;
        if (file.name === 'index.html' || file.name.endsWith('.html')) {
          const headInjection = `\n    <title>${seoTitle}</title>\n    <meta name="description" content="${seoDescription}" />${seoFavicon}${seoOgImage}${seoCustomMeta}`;
          if (content.includes('</head>')) {
            content = content.replace('</head>', `${headInjection}\n  </head>`);
          }
        }
        files.push({ file: file.name, data: content });
      }
    });
  }

  return files;
};

