deployRoutes.post('/vercel/:id', async (c) => {
  try {
    const id = c.req.param('id');
    const framework = c.req.query('framework') || 'html';
    const { token } = await c.req.json();
    
    if (!token) {
      return c.json({ error: 'Vercel token is required' }, 400);
    }

    const project = await projectStore.get(id);
    if (!project) {
      return c.json({ error: 'Project not found' }, 404);
    }

    let filesToExport = [];
    if (project.mutations && project.mutations.length > 0) {
      const latestMutation = project.mutations[project.mutations.length - 1];
      if (latestMutation.files) {
        filesToExport = latestMutation.files;
      }
    }

    if (filesToExport.length === 0) {
      return c.json({ error: 'No files generated for this project yet' }, 400);
    }

    const files = generateVercelFiles(project, framework, filesToExport);
    
    // Map framework for Vercel projectSettings
    let vercelFramework = null;
    if (framework === 'nextjs') vercelFramework = 'nextjs';
    else if (framework === 'vite') vercelFramework = 'vite';

    const projectName = project.name.replace(/[^a-z0-9]/gi, '-').toLowerCase().substring(0, 50) || 'stagework-project';

    const response = await fetch('https://api.vercel.com/v13/deployments', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${token}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        name: projectName,
        files: files,
        projectSettings: {
          framework: vercelFramework
        }
      })
    });

    const data = await response.json();

    if (!response.ok) {
      console.error('Vercel deployment failed:', data);
      return c.json({ error: data.error?.message || 'Vercel deployment failed' }, response.status as any);
    }

    return c.json({ 
      success: true, 
      url: `https://${data.url}`,
      deploymentId: data.id 
    });

  } catch (error) {
    console.error('Deploy error:', error);
    return c.json({ error: 'Failed to deploy project' }, 500);
  }
});


